  // üîß M√âTODO AUXILIAR PARA FAZER REQUISI√á√ïES
  async makeRequest(method, endpoint, data = null, description = '') {
    this.results.total++;
    
    try {
      log.info(`Testando: ${method.toUpperCase()} ${endpoint}`);
      
      const config = {
        method: method.toLowerCase(),
        url: endpoint,
        timeout: CONFIG.timeout
      };
      
      if (data && ['post', 'put', 'patch'].includes(method.toLowerCase())) {
        config.data = data;
      }
      
      const response = await this.client(config);
      
      this.results.success++;
      this.results.tests.push({
        endpoint,
        method: method.toUpperCase(),
        status: response.status,
        success: true,
        description,
        responseTime: response.headers['x-response-time'] || 'N/A',
        dataSize: JSON.stringify(response.data).length
      });
      
      log.success(`${response.status} - ${description || endpoint}`);
      
      // ‚úÖ MOSTRAR DADOS RESUMIDOS
      if (response.data) {
        this.showDataSummary(response.data, endpoint);
      }
      
      return response.data;
      
    } catch (error) {
      this.results.failed++;
      this.results.tests.push({
        endpoint,
        method: method.toUpperCase(),
        status: error.response?.status || 'TIMEOUT',
        success: false,
        description,
        error: error.message
      });
      
      log.error(`${error.response?.status || 'TIMEOUT'} - ${description || endpoint}: ${error.message}`);
      return null;
    }
  }

  // üìä MOSTRAR RESUMO DOS DADOS
  showDataSummary(data, endpoint) {
    if (!data) return;
    
    try {
      if (Array.isArray(data)) {
        console.log(`   üìä Array com ${data.length} itens`);
        if (data.length > 0) {
          console.log(`   üîç Primeiro item:`, JSON.stringify(data[0]).substring(0, 100) + '...');
        }
      } else if (typeof data === 'object') {
        const keys = Object.keys(data);
        console.log(`   üìä Objeto com ${keys.length} propriedades: [${keys.slice(0, 5).join(', ')}${keys.length > 5 ? '...' : ''}]`);
        
        // ‚úÖ MOSTRAR DADOS ESPEC√çFICOS POR TIPO DE ENDPOINT
        if (endpoint.includes('dashboard')) {
          console.log(`   üìà Dashboard: ${data.message || 'Dados carregados'}`);
        } else if (endpoint.includes('multas')) {
          console.log(`   ÔøΩÔøΩ Multas: ${data.count || data.total || 'N/A'} registros`);
        } else if (endpoint.includes('analytics')) {
          console.log(`   üìä Analytics: ${data.success ? 'Dados processados' : 'Erro nos dados'}`);
        } else if (endpoint.includes('sync')) {
          console.log(`   üîÑ Sync: ${data.success ? 'Operacional' : 'Com problemas'}`);
        }
      }
    } catch (e) {
      console.log(`   üìä Dados: ${typeof data} (${JSON.stringify(data).length} chars)`);
    }
  }

