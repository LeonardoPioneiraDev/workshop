  // üìä GERAR RELAT√ìRIO FINAL
  generateReport() {
    const endTime = Date.now();
    const duration = endTime - this.startTime;
    
    log.title('RELAT√ìRIO FINAL DE TESTES');
    
    console.log(`\n${colors.bright}üìä ESTAT√çSTICAS GERAIS:${colors.reset}`);
    console.log(`   ‚úÖ Sucessos: ${colors.green}${this.results.success}${colors.reset}`);
    console.log(`   ‚ùå Falhas: ${colors.red}${this.results.failed}${colors.reset}`);
    console.log(`   üìä Total: ${this.results.total}`);
    console.log(`   ‚è±Ô∏è  Dura√ß√£o: ${(duration / 1000).toFixed(2)}s`);
    console.log(`   üìà Taxa de Sucesso: ${((this.results.success / this.results.total) * 100).toFixed(1)}%`);
    
    console.log(`\n${colors.bright}üìã RESUMO POR CATEGORIA:${colors.reset}`);
    
    const categories = {};
    this.results.tests.forEach(test => {
      const category = this.getCategoryFromEndpoint(test.endpoint);
      if (!categories[category]) {
        categories[category] = { success: 0, failed: 0, total: 0 };
      }
      categories[category].total++;
      if (test.success) {
        categories[category].success++;
      } else {
        categories[category].failed++;
      }
    });
    
    Object.entries(categories).forEach(([category, stats]) => {
      const successRate = ((stats.success / stats.total) * 100).toFixed(1);
      const status = successRate === '100.0' ? '‚úÖ' : successRate >= '80.0' ? '‚ö†Ô∏è' : '‚ùå';
      console.log(`   ${status} ${category}: ${stats.success}/${stats.total} (${successRate}%)`);
    });
    
    // ‚úÖ SALVAR RELAT√ìRIO EM ARQUIVO
    this.saveReportToFile(duration);
    
    console.log(`\n${colors.bright}${colors.green}üéâ TESTE COMPLETO FINALIZADO!${colors.reset}`);
  }

  // üìÅ SALVAR RELAT√ìRIO EM ARQUIVO
  saveReportToFile(duration) {
    const report = {
      timestamp: new Date().toISOString(),
      duration: `${(duration / 1000).toFixed(2)}s`,
      summary: {
        total: this.results.total,
        success: this.results.success,
        failed: this.results.failed,
        successRate: `${((this.results.success / this.results.total) * 100).toFixed(1)}%`
      },
      tests: this.results.tests
    };
    
    const filename = `juridico-test-report-${new Date().toISOString().replace(/[:.]/g, '-')}.json`;
    fs.writeFileSync(filename, JSON.stringify(report, null, 2));
    
    console.log(`\nüìÅ Relat√≥rio salvo em: ${colors.cyan}${filename}${colors.reset}`);
  }

  // üè∑Ô∏è CATEGORIZAR ENDPOINTS
